package classandobject;

public class Datatypes {
	int a=10;
	double d=85.35;
	char ch='@';
	String s="Java";
	int arr[]= {12,45,63,85};
	boolean b=true;
	long l=9022413246l;
	short x = 7895;
	
	public static void main(String[] args) {
		Datatypes d=new Datatypes();
		System.out.println("Datatypes are : ");
		System.out.println("Value of A is : "+d.a);
		System.out.println("Value of D is :"+d.d);
		System.out.println("Value of Ch is :"+d.ch);
		System.out.println("Value of S is :"+d.s);
		System.out.println("Value of Arr is :"+d.arr);
		System.out.println("Value of B is : "+d.b);
		System.out.println("Value of L is : "+d.l);
		System.out.println("Value of X is : "+d.x);
		
	}
	

}
